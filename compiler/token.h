#ifndef TOKEN_H
#define TOKEN_H

#include <string>
#include <sstream>

enum Tokens {
	NONTOKEN,
	LETTER,
	DIGIT,

	// keywords
	BOOL,
	TRUE,
	FALSE,
	INT,
	IF,
	ELSE,
	RETURN,
	WHILE,
	OPERATOR,

	// operators
	SYMBOLPLUS,
	SYMBOLMINUS,
	SYMBOLASTERISK,
	SYMBOLMODULO,
	SYMBOLSLASH,
	SYMBOLCOMPARISON,
	SYMBOLEQUAL,
	SYMBOLNOTEQUAL,
	SYMBOLLESSTHAN,
	SYMBOLLESSTHANEQUAL,
	SYMBOLGREATERTHAN,
	SYMBOLGREATERTHANEQUAL,
	SYMBOLAND,
	SYMBOLOR,
	SYMBOLNOT,

	// punctuation
	SYMBOLSEMICOLON,
	SYMBOLCOMMA,
	SYMBOLLBRACKET,
	SYMBOLRBRACKET,
	SYMBOLSQUAREBRACKET,
	SYMBOLLBRACE,
	SYMBOLRBRACE,
	SYMBOLCOMMENT,
	ASSIGN,
	ENDOFFILE
};

struct Token {
	Tokens type;
	int value;
	int line;
	std::string str;
};

#endif